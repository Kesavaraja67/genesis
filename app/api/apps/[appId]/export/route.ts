// ─── ZIP builder (pure JS — no external dep) ──────────────────────────────────
// We build a valid ZIP file in memory using raw bytes.

function uint32LE(n: number): number[] {
  return [n & 0xff, (n >> 8) & 0xff, (n >> 16) & 0xff, (n >> 24) & 0xff];
}
function uint16LE(n: number): number[] {
  return [n & 0xff, (n >> 8) & 0xff];
}

function crc32(buf: Uint8Array): number {
  const table = (() => {
    const t = new Uint32Array(256);
    for (let i = 0; i < 256; i++) {
      let c = i;
      for (let j = 0; j < 8; j++) c = c & 1 ? (0xedb88320 ^ (c >>> 1)) : c >>> 1;
      t[i] = c;
    }
    return t;
  })();
  let c = 0xffffffff;
  for (let i = 0; i < buf.length; i++) c = table[(c ^ buf[i]) & 0xff] ^ (c >>> 8);
  return (c ^ 0xffffffff) >>> 0;
}

interface ZipEntry {
  name: string;
  data: Uint8Array;
  crc: number;
  offset: number;
}

function buildZip(files: { name: string; content: string }[]): Uint8Array {
  const enc = new TextEncoder();
  const entries: ZipEntry[] = [];
  const parts: Uint8Array[] = [];
  let offset = 0;

  for (const file of files) {
    const nameBytes = enc.encode(file.name);
    const data = enc.encode(file.content);
    const crc = crc32(data);

    const localHeader = new Uint8Array([
      0x50, 0x4b, 0x03, 0x04, // signature
      0x14, 0x00,             // version needed: 2.0
      0x00, 0x00,             // flags
      0x00, 0x00,             // compression: store
      0x00, 0x00,             // mod time
      0x00, 0x00,             // mod date
      ...uint32LE(crc),
      ...uint32LE(data.length),
      ...uint32LE(data.length),
      ...uint16LE(nameBytes.length),
      0x00, 0x00,             // extra field length
      ...nameBytes,
    ]);

    entries.push({ name: file.name, data, crc, offset });
    parts.push(localHeader, data);
    offset += localHeader.length + data.length;
  }

  // Central directory
  const cdParts: Uint8Array[] = [];
  let cdSize = 0;
  const cdOffset = offset;

  for (const entry of entries) {
    const nameBytes = enc.encode(entry.name);
    const cd = new Uint8Array([
      0x50, 0x4b, 0x01, 0x02, // signature
      0x14, 0x00,             // version made by
      0x14, 0x00,             // version needed
      0x00, 0x00,             // flags
      0x00, 0x00,             // compression
      0x00, 0x00,             // mod time
      0x00, 0x00,             // mod date
      ...uint32LE(entry.crc),
      ...uint32LE(entry.data.length),
      ...uint32LE(entry.data.length),
      ...uint16LE(nameBytes.length),
      0x00, 0x00,             // extra
      0x00, 0x00,             // comment
      0x00, 0x00,             // disk start
      0x00, 0x00,             // int attrs
      0x00, 0x00, 0xa4, 0x81, // ext attrs (regular file 0o100644)
      ...uint32LE(entry.offset),
      ...nameBytes,
    ]);
    cdParts.push(cd);
    cdSize += cd.length;
  }

  // End of central directory
  const eocd = new Uint8Array([
    0x50, 0x4b, 0x05, 0x06,
    0x00, 0x00,
    0x00, 0x00,
    ...uint16LE(entries.length),
    ...uint16LE(entries.length),
    ...uint32LE(cdSize),
    ...uint32LE(cdOffset),
    0x00, 0x00,
  ]);

  const allParts = [...parts, ...cdParts, eocd];
  const total = allParts.reduce((s, p) => s + p.length, 0);
  const out = new Uint8Array(total);
  let pos = 0;
  for (const p of allParts) { out.set(p, pos); pos += p.length; }
  return out;
}

// ─── Route handler ─────────────────────────────────────────────────────────────

export async function POST(
  _req: NextRequest,
  { params }: { params: Promise<{ appId: string }> }
) {
  const { appId } = await params;
  const authResult = await requireAuth();
  if (authResult instanceof NextResponse) return authResult;

  const resolved = await resolveApp(appId, authResult.user.id);
  if ("error" in resolved) return NextResponse.json({ error: resolved.error }, { status: resolved.status });

  const { app } = resolved;
  const config = app.config as unknown as GenesisConfig;

  if (!config?.models?.length) {
    return NextResponse.json({ error: "App has no models. Generate the app first." }, { status: 400 });
  }

  const normalizedModels = buildSchema(config.models);
  const appSlug = app.name.toLowerCase().replace(/[^a-z0-9-]/g, "-");

  // Collect all files for the ZIP
  const files: { name: string; content: string }[] = [];

  // Root files
  files.push({ name: "package.json", content: genPackageJson(app.name) });
  files.push({ name: "tsconfig.json", content: genTsConfig() });
  files.push({ name: "next.config.ts", content: genNextConfig() });
  files.push({ name: ".env.example", content: genEnvExample() });
  files.push({ name: "README.md", content: genReadme(app.name, config.models.map((m) => m.name)) });
  files.push({ name: ".gitignore", content: "node_modules/\n.next/\n.env.local\n.env\n" });

  // Prisma
  files.push({ name: "prisma/schema.prisma", content: genPrismaSchema(config) });

  // lib/db.ts
  files.push({ name: "lib/db.ts", content: genDbLib() });

  // App layout + global CSS
  files.push({ name: "app/layout.tsx", content: genAppLayout(app.name) });
  files.push({ name: "app/globals.css", content: genGlobalCss() });
  files.push({ name: "app/page.tsx", content: genHomePage(config) });

  // Per-model: page + API routes
  for (const model of normalizedModels) {
    const modelSlug = model.name.toLowerCase();

    // Page
    files.push({
      name: `app/${modelSlug}/page.tsx`,
      content: genModelPage(model.name, model.fields),
    });

    // API: list + create
    files.push({
      name: `app/api/${modelSlug}/route.ts`,
      content: genModelApiRoute(model.name, model.fields),
    });

    // API: get/put/delete by id
    files.push({
      name: `app/api/${modelSlug}/[id]/route.ts`,
      content: genModelIdApiRoute(model.name),
    });
  }

  // Build ZIP — convert to Buffer so NextResponse accepts it as BodyInit
  const zipBytes = buildZip(files);
  const zipBuffer = Buffer.from(zipBytes);

  return new NextResponse(zipBuffer, {
    status: 200,
    headers: {
      "Content-Type": "application/zip",
      "Content-Disposition": `attachment; filename="${appSlug}-genesis.zip"`,
      "Content-Length": String(zipBuffer.length),
    },
  });
}
