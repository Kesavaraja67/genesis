function genPrismaSchema(config: GenesisConfig): string {
  const modelBlocks = config.models.map((model) => {
    const fields = model.fields
      .map((f) => {
        const prismaType =
          f.type === "number"
            ? "Float"
            : f.type === "boolean"
            ? "Boolean"
            : f.type === "date" || f.type === "datetime"
            ? "DateTime"
            : f.type === "multiselect" || f.type === "json"
            ? "Json"
            : "String";
        const optional = f.required ? "" : "?";
        const unique = f.unique ? " @unique" : "";
        const dflt =
          f.default != null
            ? ` @default(${typeof f.default === "string" ? `"${f.default}"` : f.default})`
            : "";
        return `  ${f.name}  ${prismaType}${optional}${unique}${dflt}`;
      })
      .join("\n");

    return `model ${model.name} {\n  id        String   @id @default(cuid())\n${fields}\n  createdAt DateTime @default(now())\n  updatedAt DateTime @updatedAt\n}`;
  });

  return `generator client {
  provider = "prisma-client-js"
}

datasource db {
  provider = "postgresql"
  url      = env("DATABASE_URL")
}

${modelBlocks.join("\n\n")}
`;
}

