function genModelApiRoute(modelName: string, fields: NormalizedField[]): string {
  const requiredFields = fields.filter((f) => f.required);
  const validationBlock = requiredFields
    .map(
      (f) =>
        `  if (!body.${f.name}) return NextResponse.json({ error: "${f.label} is required" }, { status: 400 });`
    )
    .join("\n");

  const createData = fields
    .map((f) => `      ${f.name}: body.${f.name} ?? undefined`)
    .join(",\n");

  const modelVar = modelName.charAt(0).toLowerCase() + modelName.slice(1);

  return `import { NextRequest, NextResponse } from "next/server";
import { prisma } from "@/lib/db";
import { createId } from "@paralleldrive/cuid2";

// GET /api/${modelVar} — list with pagination + search
export async function GET(req: NextRequest) {
  const { searchParams } = new URL(req.url);
  const page = Number(searchParams.get("page") ?? 1);
  const limit = Math.min(100, Number(searchParams.get("limit") ?? 20));
  const search = searchParams.get("search") ?? "";

  const where = search
    ? {
        OR: [
${fields
  .filter((f) => f.type === "text" || f.type === "email")
  .map((f) => `          { ${f.name}: { contains: search, mode: "insensitive" as const } }`)
  .join(",\n")}
        ],
      }
    : {};

  const [data, total] = await Promise.all([
    prisma.${modelVar}.findMany({
      where,
      skip: (page - 1) * limit,
      take: limit,
      orderBy: { createdAt: "desc" },
    }),
    prisma.${modelVar}.count({ where }),
  ]);

  return NextResponse.json({ data, total, page, totalPages: Math.ceil(total / limit) });
}

// POST /api/${modelVar} — create
export async function POST(req: NextRequest) {
  const body = await req.json().catch(() => null);
  if (!body) return NextResponse.json({ error: "Invalid JSON" }, { status: 400 });

${validationBlock}

  const record = await prisma.${modelVar}.create({
    data: {
      id: createId(),
${createData},
    },
  });

  return NextResponse.json({ data: record }, { status: 201 });
}
`;
}

function genModelIdApiRoute(modelName: string): string {
  const modelVar = modelName.charAt(0).toLowerCase() + modelName.slice(1);
  return `import { NextRequest, NextResponse } from "next/server";
import { prisma } from "@/lib/db";

type Params = { params: Promise<{ id: string }> };

// GET /api/${modelVar}/[id]
export async function GET(_req: NextRequest, { params }: Params) {
  const { id } = await params;
  const record = await prisma.${modelVar}.findUnique({ where: { id } });
  if (!record) return NextResponse.json({ error: "Not found" }, { status: 404 });
  return NextResponse.json({ data: record });
}

// PUT /api/${modelVar}/[id]
export async function PUT(req: NextRequest, { params }: Params) {
  const { id } = await params;
  const body = await req.json().catch(() => null);
  if (!body) return NextResponse.json({ error: "Invalid JSON" }, { status: 400 });

  const record = await prisma.${modelVar}.update({ where: { id }, data: body }).catch(() => null);
  if (!record) return NextResponse.json({ error: "Not found" }, { status: 404 });
  return NextResponse.json({ data: record });
}

// DELETE /api/${modelVar}/[id]
export async function DELETE(_req: NextRequest, { params }: Params) {
  const { id } = await params;
  await prisma.${modelVar}.delete({ where: { id } }).catch(() => null);
  return new NextResponse(null, { status: 204 });
}
`;
}

